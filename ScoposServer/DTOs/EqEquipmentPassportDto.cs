// ============================================================================
// DTO (DATA TRANSFER OBJECT) ДЛЯ ПАСПОРТА ОБОРУДОВАНИЯ
// ============================================================================

using System.ComponentModel.DataAnnotations;

namespace ScoposServer.DTOs;

/// <summary>
/// DTO для отображения полного паспорта оборудования
/// Используется для детального просмотра информации об единице оборудования
/// </summary>
public class EqEquipmentPassportDto
{
    // ========================================================================
    // 1. ИДЕНТИФИКАЦИЯ ОБОРУДОВАНИЯ (ОБЯЗАТЕЛЬНЫЕ ПОЛЯ)
    // ========================================================================

    /// <summary>УНИКАЛЬНЫЙ КОД ОБОРУДОВАНИЯ</summary>
    /// <remarks>Первичный ключ. Уникальный идентификатор в системе. Пример: "EQ001"</remarks>
    public string Code { get; set; } = null!;

    /// <summary>Код 3D-МОДЕЛИ</summary>
    /// <remarks>Связь с визуализацией в 3D-системе. Пример: "MDL-1001"</remarks>
    public string? ModelCode { get; set; }

    // ========================================================================
    // 2. ОСНОВНЫЕ ХАРАКТЕРИСТИКИ
    // ========================================================================

    /// <summary>НАИМЕНОВАНИЕ ОБОРУДОВАНИЯ</summary>
    /// <remarks>Полное техническое название. Пример: "Компрессор центробежный Н-101"</remarks>
    public string? Name { get; set; }

    /// <summary>ТИП ОБОРУДОВАНИЯ</summary>
    /// <remarks>Категория оборудования. Пример: "Compressor", "Pump", "Vessel", "Column"</remarks>
    public string? Type { get; set; }

    // ========================================================================
    // 3. КЛАССИФИКАЦИЯ
    // ========================================================================

    /// <summary>КОД КЛАССА ОБОРУДОВАНИЯ</summary>
    /// <remarks>Код по классификатору. Пример: "CLS-COMP", "CLS-PUMP"</remarks>
    public string? ClassCode { get; set; }

    /// <summary>НАИМЕНОВАНИЕ КЛАССА ОБОРУДОВАНИЯ</summary>
    /// <remarks>Группа оборудования. Пример: "Компрессорное оборудование", "Насосное оборудование"</remarks>
    public string? ClassName { get; set; }

    // ========================================================================
    // 4. ИЕРАРХИЧЕСКАЯ ПРИНАДЛЕЖНОСТЬ (РОДИТЕЛЬ)
    // ========================================================================

    /// <summary>КОД РОДИТЕЛЬСКОГО ОБЪЕКТА</summary>
    /// <remarks>Вышестоящая установка/агрегат. Пример: "PLT-001"</remarks>
    public string? ParentCode { get; set; }

    /// <summary>НАИМЕНОВАНИЕ РОДИТЕЛЬСКОГО ОБЪЕКТА</summary>
    /// <remarks>Пример: "Установка гидрокрекинга", "Установка каталитического крекинга"</remarks>
    public string? ParentName { get; set; }

    // ========================================================================
    // 5. ЗАВОДСКИЕ И УЧЕТНЫЕ ДАННЫЕ
    // ========================================================================

    /// <summary>ИНВЕНТАРНЫЙ НОМЕР</summary>
    /// <remarks>Внутренний учетный номер предприятия. Пример: "INV-789-01"</remarks>
    public string? InventoryNumber { get; set; }

    /// <summary>ИЗГОТОВИТЕЛЬ (ПРОИЗВОДИТЕЛЬ)</summary>
    /// <remarks>Завод-изготовитель. Пример: "ОАО Казанькомпрессормаш"</remarks>
    public string? Manufacturer { get; set; }

    /// <summary>ЗАВОДСКОЙ СЕРИЙНЫЙ НОМЕР</summary>
    /// <remarks>Номер, присвоенный производителем. Пример: "SN-4501-2020"</remarks>
    public string? SerialNumber { get; set; }

    // ========================================================================
    // 6. ПРОИЗВОДСТВЕННЫЕ ДАТЫ
    // ========================================================================

    /// <summary>ГОД ИЗГОТОВЛЕНИЯ</summary>
    /// <remarks>Год выпуска с завода. Пример: "2020"</remarks>
    public string? ProductYear { get; set; }

    /// <summary>МЕСЯЦ ИЗГОТОВЛЕНИЯ</summary>
    /// <remarks>Месяц выпуска с завода. Пример: "Май", "Ноябрь"</remarks>
    public string? ProductMonth { get; set; }

    /// <summary>ДАТА ВВОДА В ЭКСПЛУАТАЦИЮ</summary>
    /// <remarks>Дата начала промышленной эксплуатации. Пример: "2020-05-15"</remarks>
    public DateTime? ComissioningDate { get; set; }

    // ========================================================================
    // 7. РАСПОЛОЖЕНИЕ И ПОДРАЗДЕЛЕНИЯ
    // ========================================================================

    /// <summary>НАИМЕНОВАНИЕ ФИЛИАЛА/ЦЕХА</summary>
    /// <remarks>Структурное подразделение. Пример: "Уфимский НПЗ"</remarks>
    public string? BranchName { get; set; }

    /// <summary>НАИМЕНОВАНИЕ ПРОИЗВОДСТВЕННОГО ПОДРАЗДЕЛЕНИЯ</summary>
    /// <remarks>Участок/отдел. Пример: "Нефтепереработка", "Каталитический крекинг"</remarks>
    public string? PrDepName { get; set; }

    /// <summary>МЕСТОПОЛОЖЕНИЕ/УСТАНОВКИ</summary>
    /// <remarks>Физическое расположение на объекте. Пример: "Цех №1 блок компрессии"</remarks>
    public string? Location { get; set; }

    // ========================================================================
    // 8. СОСТОЯНИЕ И СТАТУСЫ
    // ========================================================================

    /// <summary>СТАТУС ПОЛЬЗОВАТЕЛЯ</summary>
    /// <remarks>Эксплуатационный статус. Пример: "В работе", "В ремонте", "В резерве"</remarks>
    public string? UserStat { get; set; }

    /// <summary>СИСТЕМНЫЙ СТАТУС</summary>
    /// <remarks>Техническое состояние в системе. Пример: "Активный", "На обслуживании", "Списан"</remarks>
    public string? SystemStat { get; set; }
}