// ============================================================================
// DTO (DATA TRANSFER OBJECT) ДЛЯ КРАТКОЙ ИНФОРМАЦИИ ОБ ОБОРУДОВАНИИ
// ============================================================================

using System.ComponentModel.DataAnnotations;

namespace ScoposServer.DTOs;

/// <summary>
/// DTO для отображения сокращенной информации об оборудовании
/// Используется в списках, таблицах, выпадающих списках и карточках-превью
/// </summary>
public class EqEquipmentShortDto
{
    // ========================================================================
    // 1. ИДЕНТИФИКАЦИЯ (ОБЯЗАТЕЛЬНЫЕ ПОЛЯ)
    // ========================================================================

    /// <summary>УНИКАЛЬНЫЙ КОД ОБОРУДОВАНИЯ</summary>
    /// <remarks>
    /// Первичный ключ. Основной идентификатор для ссылок и операций.
    /// Пример: "EQ001", "EQ002", "EQ032"
    /// </remarks>
    public string Code { get; set; } = null!;

    /// <summary>КОД 3D-МОДЕЛИ</summary>
    /// <remarks>
    /// Связь с визуализацией в 3D-системе. Используется для перехода к модели.
    /// Пример: "MDL-1001", "MDL-1032"
    /// </remarks>
    public string? ModelCode { get; set; }

    // ========================================================================
    // 2. ОСНОВНАЯ ИНФОРМАЦИЯ
    // ========================================================================

    /// <summary>НАИМЕНОВАНИЕ ОБОРУДОВАНИЯ</summary>
    /// <remarks>
    /// Отображается в заголовках карточек и первой колонке таблиц.
    /// Пример: "Компрессор центробежный Н-101", "Насос центробежный П-201"
    /// </remarks>
    public string? Name { get; set; }

    /// <summary>КЛАСС ОБОРУДОВАНИЯ</summary>
    /// <remarks>
    /// Группировка оборудования по функциональному назначению.
    /// Используется для фильтрации и категоризации.
    /// Пример: "Компрессорное оборудование", "Насосное оборудование"
    /// </remarks>
    public string? ClassName { get; set; }

    // ========================================================================
    // 3. ТЕХНИЧЕСКИЕ ХАРАКТЕРИСТИКИ (КРАТКО)
    // ========================================================================

    /// <summary>ПРОИЗВОДИТЕЛЬ</summary>
    /// <remarks>
    /// Завод-изготовитель. Отображается в карточке оборудования.
    /// Пример: "ОАО Казанькомпрессормаш", "АО ГМС Ливгидромаш"
    /// </remarks>
    public string? Manufacturer { get; set; }

    /// <summary>ИНВЕНТАРНЫЙ НОМЕР</summary>
    /// <remarks>
    /// Учетный номер предприятия. Используется для поиска и идентификации.
    /// Пример: "INV-789-01", "INV-789-32"
    /// </remarks>
    public string? InventoryNumber { get; set; }

    // ========================================================================
    // 4. РАСПОЛОЖЕНИЕ
    // ========================================================================

    /// <summary>МЕСТОПОЛОЖЕНИЕ/УСТАНОВКИ</summary>
    /// <remarks>
    /// Физическое расположение на объекте. Отображается для ориентации.
    /// Пример: "Цех №1 блок компрессии", "Цех №7 дозировка"
    /// </remarks>
    public string? Location { get; set; }
}